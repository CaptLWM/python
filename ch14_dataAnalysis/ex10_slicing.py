# 배열의 슬라이싱
import numpy as np

b1 = np.array([10,20,30,40,50,60,70])
print(b1[1:4]) # [20 30 40]
print(b1[:3]) # [10 20 30] 3번 전까지 출력
print(b1[2:]) # [30 40 50 60 70] 2번부터

b1[2:5] = np.array([25,35,45]) #2~4 사이 값 변경
print(b1) # [10 20 25 35 45 60 70]

b2 = np.arange(10,100,10).reshape(3,3)
print(b2)
'''
[[10 20 30]
 [40 50 60]
 [70 80 90]]
'''

print(b2[1:3, 1:3])